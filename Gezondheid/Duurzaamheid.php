</button>
<div class="content">
  <p>
  Hier is een overzicht van hoe onze Pods worden vervoerd:
  <br><br>
Ons logistieke team kan ongeveer 144.000 pouches of 432.000 losse Pods in 1 container vervoeren. Dat is genoeg om tot 2.100.000 liter water smaak te geven. 
<br><br>
Als je het totaal voor drank in flessen bekijkt, kun je in 1 container net aan 35.000 plastic wegwerpflesjes van 500 ml vervoeren.
<br><br>
Je zou dus tot 119 containers van 500 ml plastic wegwerpflessen nodig hebben om dezelfde hoeveelheid water met smaak te vervoeren.
<br><br>
Kortom: het vervoer van onze Pods is tot 99% efficiënter dan het vervoer van gevulde plastic wegwerpflessen.
</p>
