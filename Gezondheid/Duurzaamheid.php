<button class="collapsible">
  <span class="icon">&#9660;</span>
  Hoeveel plast kan ik besparen met pods up pods?
</button>
<div class="content">
  <p>
  Als je air up® pods gebruikt in plaats van drankjes in plastic wegwerpflesjes, betekent dat tot 88% minder plastic afval. Hoe we aan dit cijfer komen? Blij dat je het vraagt. 
<br><br>
1 pod geeft minstens 5 liter water smaak, dus kan 5 plastic wegwerpflessen van 1 liter vervangen. Een zakje met 3 Pods geeft dus smaak aan minstens 15 liter water, en kan zo dus 15 plastic wegwerpflessen vervangen.
<br><br>
Een zakje met 3 air up® Pods weegt ongeveer 35 gram (het zakje 8 gram, elk pod 7,5 gram, de doorzichtige Pod case 1,3 gram). Voor dezelfde hoeveelheid drank in plastic wegwerpflessen is tot 10x meer plastic nodig: Ongeveer 300 gram voor 15 flessen van 1 liter.
<br><br>
In verhouding betekent dit dat onze Pods 88% minder plastic bevatten dan plastic wegwerpflessen
</p>
</div>

